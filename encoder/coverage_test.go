package encoder

// Generated embedder-coverage table (docs/task-embedding-coverage.md,
// "Coverage claim, generated not hand-maintained").
//
// A hand-maintained support list rots, so this one is defended three ways:
//
//  1. FRESHNESS — the table is rendered from the registry below and diffed against
//     the committed docs/embedder-coverage.md. Editing the doc by hand, or adding a
//     row without regenerating, turns the build red. Model-free, so it runs in CI.
//     Regenerate with `go test ./encoder -run EmbedderCoverage -update`.
//  2. GATE EXISTENCE — every row names the parity test that certifies it, and that
//     function must actually exist in this package's sources. You cannot claim a
//     model is validated without a gate.
//  3. PROPERTY TRUTH — when the checkpoint is present locally, the declared pooling
//     and dimensions are checked against what the loader actually derives from the
//     real files. CI skips (weights are gitignored); a developer with the models
//     gets the claims verified rather than trusted.

import (
	"bytes"
	"flag"
	"fmt"
	"os"
	"strings"
	"testing"
)

const coveragePath = "../docs/embedder-coverage.md"
const coverageHeader = "<!-- GENERATED by go test ./encoder -run EmbedderCoverage -update — DO NOT EDIT -->"

var updateCoverage = flag.Bool("update", false, "rewrite docs/embedder-coverage.md")

// loaderKind is which entry point loads a checkpoint — the thing that determines
// which forward runs, so it belongs in the published table.
type loaderKind int

const (
	loaderBERT  loaderKind = iota // LoadBERT: learned-absolute positions, GELU
	loaderNomic                   // Load/LoadWeightsFromFS: nomic-bert, RoPE
)

func (k loaderKind) String() string {
	if k == loaderBERT {
		return "LoadBERT"
	}
	return "Load"
}

// covRow is one certified embedder. Pooling/Dims are claims verified against the
// real checkpoint by TestEmbedderCoverage_propertiesMatchCheckpoints.
type covRow struct {
	Model     string     // HF id
	Dir       string     // testdata dir (gitignored; tests skip without it)
	Loader    loaderKind //
	Family    string     // architecture, as the loader sees it
	Pooling   string     // "mean" | "cls" | "—" (no pooling head)
	Tokenizer string     // "WordPiece" | "Unigram"
	Dims      int        //
	MRLMin    int        // smallest documented Matryoshka dim; 0 = NOT truncatable
	Gate      string     // the test function that certifies it
	Note      string     // what makes this row worth having
}

// Truncatable renders the Matryoshka column: the documented dimension range a
// caller may truncate to, or "—" when the model was not trained for it. Slicing a
// non-MRL embedding returns a unit-length vector that looks fine and retrieves
// worse, so this column exists to stop a serve layer honoring `dimensions`
// blindly. Verified by TestEmbedderCoverage_matryoshka.
func (r covRow) Truncatable() string {
	if r.MRLMin == 0 {
		return "—"
	}
	return fmt.Sprintf("%d→%d", r.Dims, r.MRLMin)
}

